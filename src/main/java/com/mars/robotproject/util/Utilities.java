    private static CihazKodlar fillCihazComplete(String dataIsle, int i) {

        CihazKodlar cihazKodlar = new CihazKodlar();
        //System.out.println(i+" "+ dataIsle);
        cihazKodlar.setSira(i);

        if (dataIsle.startsWith("@L") && dataIsle.endsWith("!")) {
            String size = dataIsle.substring(3, 5);
            cihazKodlar.setSize(CihazUtil.convertHexToInt(size) + "");

            String imei = dataIsle.substring(5, 22);
            cihazKodlar.setImei(imei);

            String dateP=CihazUtil.StringFormatDate(dataIsle.substring(22, 28),dataIsle.substring(28, 34));
            cihazKodlar.setTarih(dateP.split(" ")[0]);
            cihazKodlar.setSaat(dateP.split(" ")[1]);

            String index = dataIsle.substring(34, 35);
            cihazKodlar.setIndex(index);

            String enlem = dataIsle.substring(35, 43);
            enlem=CihazUtil.getLatitude(enlem);
            cihazKodlar.setEnlem(enlem);

            String boylam = dataIsle.substring(43, 52).substring(1);
            boylam=CihazUtil.getLongitude(boylam);
            cihazKodlar.setBoylam(boylam);

            cihazKodlar.setEnlemBoylam(enlem + "," + boylam);
            cihazKodlar.setAddress(getGeoLocationAddress(enlem + "," + boylam));

            String status = dataIsle.substring(52, 60); //4 ERLİ ÇEV         32 TANE VAR  EN SAĞDA LSB
            List<Integer> status32=CihazUtil.convertDecimalToBit(status);
            cihazKodlar.setStatus(status32.toString());
            //System.out.println("status(" + status.length() + ") HEX(" + status + ") int(" + cihazKodlar.convertHexToInt(status) + ")");

            String hiz = dataIsle.substring(60, 64);
            cihazKodlar.setHiz( CihazUtil.getHiz(hiz));

            String mesafe = dataIsle.substring(64, 70);
            cihazKodlar.setMesafe(CihazUtil.getMesafe(mesafe));


            String yon = dataIsle.substring(70, 73);
            cihazKodlar.setYon(CihazUtil.convertHexToInt(yon) + "");
            //System.out.println("yon(" + yon.length() + ") HEX(" + yon + ") int(" + cihazKodlar.convertHexToInt(yon) + ")");

            String alarm = dataIsle.substring(73, 76);
            cihazKodlar.setAlarm(CihazUtil.convertHexToInt(alarm) + "");
            //System.out.println("alarm(" + alarm.length() + ") HEX(" + alarm + ") int(" + cihazKodlar.convertHexToInt(alarm) + ")");

            if (dataIsle.length() > 77) {
                String uyduSayisi = dataIsle.substring(76, 80);
                cihazKodlar.setUyduSayisi(CihazUtil.convertHexToInt(uyduSayisi) + "");
                //System.out.println("uyduSayisi(" + uyduSayisi.length() + ") HEX(" + uyduSayisi + ") int(" + cihazKodlar.convertHexToInt(uyduSayisi) + ")");

                if (dataIsle.length() > 81) {
                    String pilyuzdesi = dataIsle.substring(80, 84);
                    cihazKodlar.setPilYuzdesi(CihazUtil.convertHexToInt(pilyuzdesi) + "");
                    //System.out.println("pilyuzdesi(" + pilyuzdesi.length() + ") HEX(" + pilyuzdesi + ") int(" + cihazKodlar.convertHexToInt(pilyuzdesi) + ")");
                }
            }
            cihazKodlar.setLMessage(dataIsle);
        }
        return cihazKodlar;
    }
